

void PrintImage(int[,] usersImageToPrint)
{
    for (int row = 0; row < usersImageToPrint.GetLength(0); row++)
    {
        for (int column = 0; column < usersImageToPrint.GetLength(1); column++)
        {
            if (usersImageToPrint[row, column] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}


void FillImage(int[,] userImage, int iRow, int iCol)
{
    if (userImage[iRow, iCol] == 0)
    {
        userImage[iRow, iCol] = 1;
        FillImage(userImage, iRow - 1, iCol);
        FillImage(userImage, iRow, iCol - 1);
        FillImage(userImage, iRow + 1, iCol);
        FillImage(userImage, iRow, iCol + 1);
    }
}


